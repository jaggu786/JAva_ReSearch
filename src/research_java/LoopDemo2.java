package research_java;

public class LoopDemo2
{

	public static void main(String[] args)
	{
		
printpyramid(9,"*");

 
	}

	private static void printpyramid(int no, String string) 
	
	{
		  for(int k=1;k<=no;k++)
		  {  
			  
			  
		for(int i=1;i<=no;i++)
		{
			
		
				System.out.print(string);
			
		}
		 System.out.println();
		
		
	}
	}
}
